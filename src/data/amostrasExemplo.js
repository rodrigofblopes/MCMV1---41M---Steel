import { imovelSujeitoExemplo } from './imovelSujeitoExemplo.js'

/**
 * Amostras iniciais — comparáveis no entorno do empreendimento (referência Bairro Novo / Sevilha).
 * Unidades 1–13 e lotes 14–17 no mesmo entorno (~80–280 m) para cluster no mapa.
 */
const SUJEITO_LAT = Number(imovelSujeitoExemplo.lat)
const SUJEITO_LNG = Number(imovelSujeitoExemplo.lng)
const M_POR_GRAU_LAT = 111_320
const M_POR_GRAU_LNG = 111_320 * Math.cos((SUJEITO_LAT * Math.PI) / 180)

/** Metros a norte (+) e leste (+) a partir do ponto do empreendimento. */
function pos(norteM, lesteM) {
  return {
    lat: Math.round((SUJEITO_LAT + norteM / M_POR_GRAU_LAT) * 1e7) / 1e7,
    lng: Math.round((SUJEITO_LNG + lesteM / M_POR_GRAU_LNG) * 1e7) / 1e7,
  }
}

